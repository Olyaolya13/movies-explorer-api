const errorServer = (err, req, res, next) => {
  const { statusCode = 500, message } = err;

  res
    .status(statusCode)
    .send({
      message: statusCode === 500
        ? 'На сервере произошла ошибка'
        : message,
    });

  if (statusCode !== 500) {
    next(err);
  }
};

module.exports = errorServer;
